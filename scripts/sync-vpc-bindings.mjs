#!/usr/bin/env node
//
// Generates `src/vpc.generated.ts` from the VPC binding declarations in
// `wrangler.jsonc`.
//
// Why this exists:
//   The dashboard's VPC page and the egress-policy editor's "VPC service"
//   binding dropdown both need to know the bindings declared in
//   wrangler.jsonc. The Worker can't read wrangler.jsonc at runtime, so we
//   used to ask users to mirror the metadata into a `VPC_BINDINGS_JSON`
//   var. That meant editing two places and silently drifting whenever
//   someone forgot.
//
//   This script makes wrangler.jsonc the single source of truth: it scans
//   the `vpc_networks` and `vpc_services` arrays and emits a tiny TS
//   module the Worker imports at build time. Run via the `prebuild`
//   script in package.json so it stays in sync automatically.
//
// What it does:
//   1. Read wrangler.jsonc from the repo root.
//   2. Strip JSONC comments (// line and /* block */) and trailing
//      commas — Node's JSON.parse can't handle them.
//   3. Pull each entry from `vpc_networks[]` (type: "network", id:
//      network_id) and `vpc_services[]` (type: "service", id:
//      service_id).
//   4. Write the union to `src/vpc.generated.ts` as a typed const array.
//
// What it deliberately doesn't do:
//   - No description field. wrangler.jsonc has no place to put one and we
//     don't want a sidecar config. The dashboard renders binding/type/id
//     which is enough.
//   - No validation beyond "binding is a string and the id field exists."
//     wrangler itself validates the rest.

import { readFileSync, writeFileSync, mkdirSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const here = dirname(fileURLToPath(import.meta.url));
const repoRoot = resolve(here, "..");
const wranglerPath = resolve(repoRoot, "wrangler.jsonc");
const outputPath = resolve(repoRoot, "src/vpc.generated.ts");

// Strip JSONC commentary and trailing commas so JSON.parse accepts it.
// Naive but sufficient — wrangler.jsonc is hand-edited config, not user
// input. We avoid pulling in a real JSONC parser to keep the script
// dependency-free.
function stripJsonc(text) {
  let out = "";
  let i = 0;
  let inString = false;
  let stringQuote = "";
  while (i < text.length) {
    const ch = text[i];
    const next = text[i + 1];
    // Track strings so we don't strip "//" inside a value.
    if (inString) {
      out += ch;
      if (ch === "\\" && i + 1 < text.length) {
        out += text[i + 1];
        i += 2;
        continue;
      }
      if (ch === stringQuote) inString = false;
      i += 1;
      continue;
    }
    if (ch === '"' || ch === "'") {
      inString = true;
      stringQuote = ch;
      out += ch;
      i += 1;
      continue;
    }
    // Line comment: drop everything up to the next newline.
    if (ch === "/" && next === "/") {
      const nl = text.indexOf("\n", i);
      i = nl === -1 ? text.length : nl;
      continue;
    }
    // Block comment: drop everything up to the closing */.
    if (ch === "/" && next === "*") {
      const end = text.indexOf("*/", i + 2);
      i = end === -1 ? text.length : end + 2;
      continue;
    }
    out += ch;
    i += 1;
  }
  // Trailing commas before } or ].
  return out.replace(/,(\s*[}\]])/g, "$1");
}

const raw = readFileSync(wranglerPath, "utf8");
let config;
try {
  config = JSON.parse(stripJsonc(raw));
} catch (err) {
  console.error(`[sync-vpc] failed to parse ${wranglerPath}: ${err.message}`);
  process.exit(1);
}

const bindings = [];

// vpc_networks → { binding, type: "network", id: network_id }
for (const entry of config.vpc_networks || []) {
  if (!entry || typeof entry.binding !== "string") continue;
  if (typeof entry.network_id !== "string") continue;
  bindings.push({ binding: entry.binding, type: "network", id: entry.network_id });
}

// vpc_services → { binding, type: "service", id: service_id }
for (const entry of config.vpc_services || []) {
  if (!entry || typeof entry.binding !== "string") continue;
  if (typeof entry.service_id !== "string") continue;
  bindings.push({ binding: entry.binding, type: "service", id: entry.service_id });
}

// Stable order: networks first, then services, then alphabetical by
// binding name. Keeps the generated file diff-clean across runs.
bindings.sort((a, b) => {
  if (a.type !== b.type) return a.type === "network" ? -1 : 1;
  return a.binding.localeCompare(b.binding);
});

const header = `// Auto-generated by scripts/sync-vpc-bindings.mjs from wrangler.jsonc.
// Do not edit by hand — your changes will be overwritten on the next
// \`npm run build\`. To add or remove a binding, edit wrangler.jsonc.

import type { VpcBinding } from "./vpc";

export const VPC_BINDINGS: VpcBinding[] = ${JSON.stringify(bindings, null, 2)};
`;

mkdirSync(dirname(outputPath), { recursive: true });
writeFileSync(outputPath, header);

console.log(
  `[sync-vpc] wrote ${bindings.length} binding${bindings.length === 1 ? "" : "s"} to ${outputPath}`,
);
